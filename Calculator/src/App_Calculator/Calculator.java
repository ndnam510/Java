    public void giaiptb2(double a, double b, double c)
    {
        double d;
        double kq1, kq2;
        if(a!=0)
        {
            d = b*b - 4*a*c;
            if(d>0)
            {
                TxtSoNg.setText("Có 2 nghiệm");
                txtKq.setEnabled(true);
                txtKq1.setEnabled(Boolean.TRUE);
                kq1 =(-b-sqrt(d))/(2*a);
                kq2 = (-b+sqrt(d))/(2*a);
                if(abs(kq1) % 1 >= 0.9999999999)
                {
                    if(kq1 < 0) kq1 = (int)kq1 - 1;
                    else kq1 = (int)kq1 + 1;
                }
                if(abs(kq2) % 1 >= 0.9999999999)
                {
                    if(kq2 < 0) kq2 = (int)kq2 - 1;
                    else kq2 = (int)kq2 + 1;
                }
                txtKq.setText(String.valueOf(kq1));
                txtKq1.setText(String.valueOf(kq2));
            }
            else if(d==0)
            {
                TxtSoNg.setText("Có nghiệm kép");
                txtKq.setEnabled(true);
                kq1 =(-b+sqrt(d))/(2*a);
                if(abs(kq1) % 1 >= 0.9999999999)
                {
                    if(kq1 < 0) kq1 = (int)kq1 - 1;
                    else kq1 = (int)kq1 + 1;
                }
                txtKq.setText(String.valueOf(kq1));
            }
            else if(d<0) TxtSoNg.setText("Vô nghiệm");
        }
        else if(a==0 && b!=0)
        {
            TxtSoNg.setText("Có 1 nghiệm");
            txtKq.setEnabled(true);
            kq1 =(-c)/(b);
            if(kq1==-0) kq1 = 0;
            if(abs(kq1) % 1 >= 0.9999999999)
                {
                    if(kq1 < 0) kq1 = (int)kq1 - 1;
                    else kq1 = (int)kq1 + 1;
                }
            txtKq.setText(String.valueOf(kq1));
        }
        else if(a==0 && b==0 && c!=0)
        {
            TxtSoNg.setText("Vô nghiệm");
        }
        else if(a==0 && b==0 && c==0)
        {
            TxtSoNg.setText("Vô số nghiệm");
        }
    }
