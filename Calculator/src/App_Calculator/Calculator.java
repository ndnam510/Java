    public void giaiptb3(double a, double b, double c, double d)
    {
        double dt = b*b - 3*a*c;
        double k, kq, kq1, kq2;
        k = (9*a*b*c - 2*Math.pow(b,3) - 27*a*a*d)/(2*sqrt(pow(abs(dt),3)));
        
        if(dt > 0)
        {
            if(abs(k) <= 1)
            {
                TxtSoNg.setText("có 3 nghiệm (dt>0 & k<=1)");
                txtKq.setEnabled(true);
                txtKq1.setEnabled(true);
                txtKq2.setEnabled(true);
                kq = (2*sqrt(dt)*cos((acos(k))/3) - b) / (3*a);
                kq1 = (2*sqrt(dt)*cos((acos(k))/3 - 2*PI/3) - b) / (3*a);
                kq2 = (2*sqrt(dt)*cos((acos(k))/3 + 2*PI/3) - b) / (3*a);
                if(abs(kq) % 1 >= 0.9999999999)
                {
                    if(kq < 0) kq = (int)kq - 1;
                    else kq = (int)kq + 1;
                }
                if(abs(kq1) % 1 >= 0.9999999999)
                {
                    if(kq1 < 0) kq1 = (int)kq1 - 1;
                    else kq1 = (int)kq1 + 1;
                }
                if(abs(kq2) % 1 >= 0.9999999999)
                {
                    if(kq2 < 0) kq2 = (int)kq2 - 1;
                    else kq2 = (int)kq2 + 1;
                }
                txtKq.setText(String.valueOf(kq));
                txtKq1.setText(String.valueOf(kq1));
                txtKq2.setText(String.valueOf(kq2));
            }
            else
            {
                TxtSoNg.setText("có 1 nghiệm (dt>0 & k>1)");
                txtKq.setEnabled(true);
                kq = ((sqrt(dt)*abs(k))/(3*a*k))
                     * (pow((abs(k)) + sqrt(k*k - 1), 1.0/3)
                      + pow((abs(k)) - sqrt(k*k - 1), 1.0/3))
                       - (b /(3*a));
                if(abs(kq) % 1 >= 0.9999999999)
                {
                    if(kq < 0) kq = (int)kq - 1;
                    else kq = (int)kq + 1;
                }
                txtKq.setText(String.valueOf(kq));
            }
        }
        else if(dt == 0)
        {
            TxtSoNg.setText("có 1 nghiệm bội (dt==0)");
            txtKq.setEnabled(true);
            kq = ((-b) + (pow((pow(b,3) - 27*a*a*d), 1.0/3))) / (3*a);
            if(abs(kq) % 1 >= 0.9999999999)
                {
                    if(kq < 0) kq = (int)kq - 1;
                    else kq = (int)kq + 1;
                }
            txtKq.setText(String.valueOf(kq));
        }
        else if(dt < 0)
        {
            TxtSoNg.setText("có 1 nghiệm (dt<0)");
            txtKq.setEnabled(true);
            kq = (sqrt(abs(dt))/(3*a))
                     * (sqrtn(k + sqrt(k*k + 1), 3)
                      + sqrtn(k - sqrt(k*k + 1), 3))
                       - (b /(3*a));
            if(abs(kq) % 1 >= 0.9999999999)
                {
                    if(kq < 0) kq = (int)kq - 1;
                    else kq = (int)kq + 1;
                }
            txtKq.setText(String.valueOf(kq));
        }
    }
